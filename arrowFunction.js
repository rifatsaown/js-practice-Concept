//normal Function example
function addab(a, b) {
    return a + b;
}
console.log(addab(1, 2));

//arrow function example
const addNumber = (a, b) => {
    return a + b;
}
console.log(addNumber(1, 2));

//arrow function example without parameter
const add = () => {
    return 1 + 2;
}
console.log(add());