//Ternary Operator example
const age = 18;
const message = age > 18 ? 'Welcome' : 'Sorry';
console.log(message);

//Ternary Operator example with multiple condition
const age = 18;
const message = age > 18 && age < 30 ? 'Welcome' : 'Sorry';
console.log(message);

//Ternary Operator example with multiple condition
const age = 18;
const message = age > 18 || age < 30 ? 'Welcome' : 'Sorry';
console.log(message);